package PojoClassPractice;

public class Spause1 {
String sname;
int sid;
int sphNo;
public Spause1(String sname, int sid, int sphNo) {
	super();
	this.sname = sname;
	this.sid = sid;
	this.sphNo = sphNo;
}
public Spause1()
{
	
}
public String getSname() {
	return sname;
}
public void setSname(String sname) {
	this.sname = sname;
}
public int getSid() {
	return sid;
}
public void setSid(int sid) {
	this.sid = sid;
}
public int getSphNo() {
	return sphNo;
}
public void setSphNo(int sphNo) {
	this.sphNo = sphNo;
}

}
